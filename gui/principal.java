    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int codtrabajador = Integer.parseInt(jTextField9.getText());
        int codpista = Integer.parseInt(jTextField12.getText());
        int numedicion = Integer.parseInt(jTextField10.getText());
        int anoedicion = Integer.parseInt(jTextField11.getText());
        Timestamp fechainicio, fechafin;
        
        try{
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
            Date parsedDate = dateFormat.parse(jTextField13.getText());
            fechainicio = new java.sql.Timestamp(parsedDate.getTime());
            parsedDate = dateFormat.parse(jTextField14.getText());
            fechafin = new java.sql.Timestamp(parsedDate.getTime());
            
            boolean ok = db.asignarTrabajador(codtrabajador, numedicion, anoedicion, codpista, fechainicio, fechafin);
            if(ok) jButton3.setBackground(new Color(50,205,50));
            else   jButton3.setBackground(new Color(255,0,0));
        }
        catch(Exception e){
            System.out.println("Error asignar trabajador" + e);
            jButton3.setBackground(new Color(255,0,0));
        }

    }//GEN-LAST:event_jButton3ActionPerformed

