    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int codentrada = Integer.parseInt(jTextField1.getText());
        String tipo = jTextField2.getText();
        int precio = Integer.parseInt(jTextField3.getText());
        
        try{
            boolean ok = db.addTipoEntrada(codentrada, tipo, precio);
            if(ok) jButton1.setBackground(new Color(50,205,50));
            else   jButton1.setBackground(new Color(255,0,0));
        }
        catch(Exception e){
            System.out.println("Error añadir entrada" + e);
            jButton1.setBackground(new Color(255,0,0));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

